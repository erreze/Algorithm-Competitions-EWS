    public int minimumMoves(int A, int B) {
        Queue<Point> Q = new LinkedList<Point>();
        Q.add(new Point(A,0));
        HashSet<Integer> memo=new HashSet<Integer>();
        while(!Q.isEmpty()){
//        	pr(Q);
        	Point p=Q.poll();
        	if(p.x==B){
        		return p.y;
        	}
        	int rev=rev(p.x);
        	
        	if(!memo.contains(rev)){
        		memo.add(rev);
        		Q.add(new Point(rev,p.y+1));
        	}
        	int di=p.x/10;
        	if(!memo.contains(di)){
        		memo.add(di);
        		Q.add(new Point(di,p.y+1));
        	}
        }
        return -1;
    }
    int rev(int a){
    	int r=0;
    	while(a>0){
    		int d=a%10;
    		a/=10;
    		r=r*10+d;
    	}
    	return r;
    }
